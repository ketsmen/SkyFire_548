void Spell::EffectNULL(SpellEffIndex effIndex)
{
    Skyfire::Spells::SpellDispatchValidationStatus status = Skyfire::Spells::ClassifySpellDispatchHandler(Skyfire::Spells::SPELL_DISPATCH_HANDLER_NULL);
    SF_LOG_DEBUG("spells", "Spell %u effect index %u effect %u dispatch status: %s",
        m_spellInfo->Id, uint32(effIndex), m_spellInfo->Effects[effIndex].Effect, Skyfire::Spells::GetSpellDispatchValidationStatusName(status));
}

void Spell::EffectUnused(SpellEffIndex effIndex)
{
    Skyfire::Spells::SpellDispatchValidationStatus status = Skyfire::Spells::ClassifySpellDispatchHandler(Skyfire::Spells::SPELL_DISPATCH_HANDLER_UNUSED);
    SF_LOG_DEBUG("spells", "Spell %u effect index %u effect %u dispatch status: %s",
        m_spellInfo->Id, uint32(effIndex), m_spellInfo->Effects[effIndex].Effect, Skyfire::Spells::GetSpellDispatchValidationStatusName(status));
}
